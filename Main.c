#include<stdio.h>
#include<conio.h>
#include<math.h>
#define N 68 //Size of the signal
float x[N] = {1.000000,1.290524,1.544709,1.738663,1.853100,1.875078,1.799183,1.628051,1.372170,1.048990,0.681422,0.295850,-0.080163,-0.420268,-0.701609,-0.906793,-1.025364,-1.054659,-0.999966,-0.873975,-0.695562,-0.488032,-0.276959,-0.087837,0.056274,0.136856,0.141655,0.065818,-0.087623,-0.307987,-0.577872,-0.874678,-1.172619,-1.445013,-1.666671,-1.816151,-1.877695,-1.842654,-1.710299,-1.487920,-1.190222,-0.838061,-0.456645,-0.073331,0.284735,0.592983,0.831539,0.986918,1.053156,1.032261,0.933954,0.774723,0.576275,0.363521,0.162276,-0.003112,-0.111999,-0.149352,-0.107143,0.014890,0.209176,0.460950,0.749557};//Signal Input
double y[N] = {0};//Output frequency domain in y
double y_single[N/2]={0};//Output frequency domain in single sided

int main()
{
    int i=0,j=0;
    double real=0,img=0;
    for(i=0;i<N;i++)
    {
       for(j=0;j<N;j++)
       {
            real+=cos(2*3.141592654*i*j/N)*x[j];
            img+=sin(2*3.141592654*i*j/N)*x[j]*-1;
       }
       y[i]=sqrt(real*real + img*img);
       //printf("%f\n",y[i]);
       real=0;
       img=0;
    }
    y_single[0]=y[N/2];
    for(i=1;i<N/2;i++)
    {
        y_single[N/2-i]=y[N/2-i]+y[N/2+i];
    }
    for(i=0;i<N/2;i++)
    {
        printf("%f\n",y_single[i]);
    }
    return 0;
}
